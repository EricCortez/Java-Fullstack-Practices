public class Customer {
    private int id;
    private  String name;
    private  String dob;
    private  double salary;
    private String email;
    private  int age;



    public Customer(int id, String name, String dob, double salary, String email, int age) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.salary = salary;
        this.email = email;
        this.age = age;
    }


}
